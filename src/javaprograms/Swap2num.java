package javaprograms;

public class Swap2num {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("the value of a is "+a);
		System.out.println("the value of b is "+b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("the value of a is "+a);
		System.out.println("the value of b is "+b);
	}

}
